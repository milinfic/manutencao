    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        servico_crud crudservico = new servico_crud();
        if (confirmar == 0) {
            confirmar++;

            int OS = Integer.parseInt(jFormattedTextFieldOS.getText());
            boolean v = crudservico.consultaOS(OS);

            if (v == true) {
                ArrayList<equipamento_model> lista = crudservico.buscaEntrada(OS);

                jFormattedTextFieldCPF.setText(lista.get(0).getCpf());
                jTextFieldNome.setText(lista.get(0).getNome());
                jComboBoxTipo.setSelectedItem(lista.get(0).getTipo());
                jTextFieldMarca.setText(lista.get(0).getMarca());
                jTextFieldModelo.setText(lista.get(0).getModelo());
                jTextFieldDescricao.setText(lista.get(0).getIdentificacao());

                ArrayList<servico_model> listaservico = crudservico.buscaservico(OS);

                jFormattedTextFieldDataEntrada.setText(listaservico.get(0).getDataentrada());
                jComboBoxSituacao.setSelectedIndex(listaservico.get(0).getSituacao());
                jFormattedTextFieldDataSaida.setText(listaservico.get(0).getDatasaida());
                jTextPaneDescricao.setText(listaservico.get(0).getDescricao());
                jFormattedTextFieldTotalProduto.setText(String.valueOf(listaservico.get(0).getTotalservico()));
                jFormattedTextFieldValorTotalGeral.setText(String.valueOf(listaservico.get(0).getValornota()));

                estoque_crud crud = new estoque_crud();
                estoque = crud.buscarestoque();
                jComboBoxTipoPeca.removeAllItems();
                for (int i = 0; i < estoque.size(); i++) {
                    jComboBoxTipoPeca.addItem(String.valueOf(estoque.get(i).getIdproduto()));
                }

                visualizar();

            } else {
                JOptionPane.showMessageDialog(null, "Número de OS não encontrada! \n"
                        + "Verifique o valor digitado.");
            }
        } else {
            //int OS = Integer.parseInt(jFormattedTextFieldOS.getText());
            boolean v = crudservico.consultaOS(Integer.parseInt(jFormattedTextFieldOS.getText()));
            if (v == true) {
                dispose();
                Tela_Saida x = new Tela_Saida(jFormattedTextFieldOS.getText());
                x.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Número de OS não encontrada! \n"
                        + "Verifique o valor digitado.");
            }

        }


    }//GEN-LAST:event_jButtonBuscarActionPerformed

