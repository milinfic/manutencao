    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        if (jFormattedTextFieldQuantidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Digite uma quantidade válidada \n"
                    + "Valor tem que ser número e maior do que zero");
        } else {

            servicodetalhado_crud crud = new servicodetalhado_crud();
            servicodetalhado_model model = new servicodetalhado_model();

            double valortotal;
            int quantidade, idproduto;

            model.setIdservico(Integer.parseInt(jFormattedTextFieldOS.getText()));
            idproduto = Integer.parseInt(jComboBoxTipoPeca.getSelectedItem().toString());
            model.setIdproduto(idproduto);
            idproduto--;
            model.setDescricaoitem(jTextFieldDescricaoPecas.getText());
            quantidade = Integer.parseInt(jFormattedTextFieldQuantidade.getText());
            model.setQuantidadade(quantidade);
            valortotal = Double.parseDouble(jFormattedTextFieldValorunitario.getText().replaceAll(",", "."));
            valortotal = valortotal * quantidade;
            model.setValortotal(valortotal);

            crud.inserir(model);
            visualizar();
            valortotal();

        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

