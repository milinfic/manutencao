    public void visualizar() {
        int os = Integer.parseInt(jFormattedTextFieldOS.getText());
        String valorFormatado = "0";
        boolean v = false;
        servicodetalhado_crud crud = new servicodetalhado_crud();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        item = crud.buscaitem(os);
        double totalprodutos = 0;

        for (int i = 0; i < item.size(); i++) {
            valorFormatado = new DecimalFormat("#0.00").format(item.get(i).getValortotal());
            modelo.addRow(new Object[]{
                item.get(i).getDescricaoitem(),
                item.get(i).getQuantidadade(),
                "RS " + valorFormatado//item.get(i).getValortotal()
            });
            //totalprodutos = totalprodutos + item.get(i).getValortotal();
            if (item.get(i).getIdproduto() == 1) {
                jFormattedTextFieldMaodeObra.setText(
                        String.valueOf(item.get(i).getValortotal()));
                v = true;
            } else {
                totalprodutos = totalprodutos + item.get(i).getValortotal();
            }
        }
        if (v == false) {
            jFormattedTextFieldMaodeObra.setText("0");
        }
        jFormattedTextFieldTotalProduto.setText(String.valueOf(totalprodutos));
        valortotal();
    }

    public void valortotal() {
        double valortotal;
        String totalvalor = jFormattedTextFieldTotalProduto.getText().replaceAll(",", ".");
        String manutencao = jFormattedTextFieldMaodeObra.getText().replaceAll(",", ".");
        valortotal = Double.parseDouble(totalvalor)
                + Double.parseDouble(manutencao);
        //String valor = new DecimalFormat("#,##0.00").format(valortotal);
        jFormattedTextFieldValorTotalGeral.setText(String.valueOf(valortotal));
        //jFormattedTextFieldValorTotalGeral.setText(String.valueOf(valortotal));
    }

